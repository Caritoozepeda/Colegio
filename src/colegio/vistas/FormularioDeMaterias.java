    private void jBtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarActionPerformed
        // TODO add your handling code here:

        String nom = jtxtNombre.getText();
        int cod = Integer.parseInt(jtxtCodigo.getText());
        int anio = Integer.parseInt(jtxtAnio.getText());

        Materia mat = new Materia(cod, nom, anio);
        materias.add(mat);
        JOptionPane.showMessageDialog(this, " Se agregó la materia ");
        desactivar();


    }//GEN-LAST:event_jBtnGuardarActionPerformed


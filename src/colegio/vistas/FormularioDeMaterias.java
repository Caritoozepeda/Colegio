    private void jtxtCodigoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtCodigoKeyTyped
        // TODO add your handling code here:

        int key = evt.getKeyChar();
        boolean numeros = key >= 48 && key <= 57;
        if (!numeros) {
//            JOptionPane.showMessageDialog(this,"El dato CODIGO debe ser numérico");
            evt.consume();
        }
    }//GEN-LAST:event_jtxtCodigoKeyTyped

    private void jtxtAnioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtAnioKeyTyped
        // TODO add your handling code here:
        int key = evt.getKeyChar();
        boolean numeros = key >= 48 && key <= 57;
        if (!numeros) {
            evt.consume();
        }

    }//GEN-LAST:event_jtxtAnioKeyTyped

    private void jtxtNombreFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtxtNombreFocusLost
        // TODO add your handling code here:
        if (jtxtNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El campo Nombre debe estar completo");
            jtxtNombre.requestFocus();
        }

    }//GEN-LAST:event_jtxtNombreFocusLost

    private void jtxtAnioFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtxtAnioFocusLost
        // TODO add your handling code here:
        if (jtxtAnio.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El campo Año debe estar completo");
            jtxtAnio.requestFocus();
        }
        if (!jtxtNombre.getText().isEmpty()) {
            if (!jtxtAnio.getText().isEmpty()) {
                if (!jtxtCodigo.getText().isEmpty()) {
                    jBtnGuardar.setEnabled(true);
                }
            }
        }

    }//GEN-LAST:event_jtxtAnioFocusLost

